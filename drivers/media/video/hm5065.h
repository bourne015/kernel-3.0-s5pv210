struct hm5065_reg {
	unsigned char addr1;
	unsigned char addr2;
	unsigned short val;
};

struct hm5065_regset_type {
	unsigned char *regset;
	int len;
};

/*
 * Macro
 */
#define REGSET_LENGTH(x)	(sizeof(x)/sizeof(hm5065_reg))

/*
 * User defined commands
 */
/* S/W defined features for tune */
#define REG_DELAY	0xAB	/* in ms */
#define REG_CMD		0xFFFF	/* Followed by command */

/* Following order should not be changed */
enum image_size_hm5065 {
	/* This SoC supports upto SXGA (1280*1024) */
	QQVGA, /* 160*120*/
	QCIF, /* 176*144 */
	QVGA, /* 320*240 */
	CIF, /* 352*288 */
	VGA, /* 640*480 */
	SVGA, /* 800*600 */
	HD720P, /* 1280*720 */
	SXGA, /* 1280*1024 */
};

/*
 * Following values describe controls of camera
 * in user aspect and must be match with index of hm5065_regset[]
 * These values indicates each controls and should be used
 * to control each control
 */
